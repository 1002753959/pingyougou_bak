import cn.itcast.core.service.StaticPageService;
import cn.itcast.core.service.StaticPageServiceImpl;
import org.apache.activemq.command.ActiveMQTextMessage;
import org.springframework.beans.factory.annotation.Autowired;

import javax.jms.JMSException;
import javax.jms.Message;
import javax.jms.MessageListener;

/**
 * @Author: Chenqi
 * @Description:
 * @Date: Create in 11:35 2018/12/16
 */
public class PageListener implements MessageListener {
    @Autowired
    private StaticPageService staticPageService;

    @Override
    public void onMessage(Message message) {
        ActiveMQTextMessage ATM = (ActiveMQTextMessage) message;
        try {
            //获取到Id
            String id = ATM.getText();
            System.out.println("静态化项目接收到的Id:" + id);

            //TODO 3.为这些商品创建静态页面
            staticPageService.StaticPage(Long.parseLong(id));
        } catch (JMSException e) {
            e.printStackTrace();
        }
    }
}

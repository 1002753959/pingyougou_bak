import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

import java.util.List;

@RestController
@RequestMapping("/orderStat")
public class OrderStatController {
    @Reference
    private OrderStatServie orderStatServie;

  @RequestMapping("/search")
//  time 表示时间区间的标识.1当天内  2一周内  3一月内  4半年内  5一年内  6两年内
  public List<GoodsTime> search( int time) {
      String name = SecurityContextHolder.getContext().getAuthentication().getName();
   return orderStatServie.search( time, name);

  }

}
